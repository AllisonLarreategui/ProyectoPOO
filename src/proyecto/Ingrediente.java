public class Ingrediente {
    String nombre;
    int calorias;
    int hidratos;
    int proteinas;
    int grasas;
    int fibras;
    
    public Ingrediente(String nombre, int calorias, int hidratos, int proteinas, int grasas, int fibras) {
        this.nombre = nombre;
        this.calorias = calorias;
        this.hidratos = hidratos;
        this.proteinas = proteinas;
        this.grasas = grasas;
        this.fibras = fibras;}
    
    
}
